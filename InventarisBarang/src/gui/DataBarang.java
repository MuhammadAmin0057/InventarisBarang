    private void tDataBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tDataBarangMouseClicked
        int i = tDataBarang.getSelectedRow();
        
        if (i>-1){
            tfKode.setText(model.getValueAt(i, 0).toString());
             tfNama.setText(model.getValueAt(i, 1).toString());
              tfTanggal.setText(model.getValueAt(i, 2).toString());
            }
    }//GEN-LAST:event_tDataBarangMouseClicked

