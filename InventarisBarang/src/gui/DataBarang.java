private void tampilkan() {
        int row = tDataBarang.getRowCount();
        for(int a = 0; a<row;a++){
        model.removeRow(0);
        }

    try {
        // mengconnectkan netbeans ke database mysql
       Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventarisbarang","root","");
        ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM data_barang");
        while(rs.next()){
            String data []= {rs.getString(1),rs.getString(2),rs.getString(3)};
            model.addRow(data);
        }
    } catch (SQLException ex) {
        Logger.getLogger(DataBarang.class.getName()).log(Level.SEVERE, null, ex);
    }

    }

